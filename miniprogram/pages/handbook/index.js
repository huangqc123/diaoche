  // 切换章节展开
  toggleChapter(e) {
    const id = e.currentTarget.dataset.id
    this.setData({
      activeChapter: this.data.activeChapter === id ? '' : id,
      expandedSections: {} // 切换章节时重置section
    })
  },

  // 切换section展开
  toggleSection(e) {
    const key = e.currentTarget.dataset.key
    const expanded = { ...this.data.expandedSections }
    expanded[key] = !expanded[key]
    this.setData({ expandedSections: expanded })
  },

  // 搜索
  onSearchInput(e) {
    this.setData({ searchKey: e.detail.value })
  },

  clearSearch() {
    this.setData({ searchKey: '' })
  },

  // 快速跳转到指定章节
  jumpToChapter(e) {
    const id = e.currentTarget.dataset.id
    this.setData({
      activeChapter: id,
      expandedSections: {}
    })
    // 滚动到对应位置
    wx.pageScrollTo({
      selector: '#chapter-' + id,
      duration: 300
    })
  },

